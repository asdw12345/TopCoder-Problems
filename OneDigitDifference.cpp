typedef long long LL;
class OneDigitDifference {
public:
	int getSmallest(int);
};
LL __pow(int a,int n)
{
    LL ans=1;
    for(int i=1;i<=n;i++) ans*=a;
    return ans;
}
int OneDigitDifference::getSmallest(int N)
{
   int i,j;
   int pos;
   for(i=1;;i++)
   {
       if(__pow(10,i)==N)
       {
           pos=i+1;
           break;
       }
       else if(__pow(10,i)>N)
       {
           pos=i;
           break;
       }
   }
   if(pos>=2)
   {
       for(i=pos-1;i>=0;i--)
       {
           if(N%__pow(10,i)!=N%__pow(10,i-1)) return N%__pow(10,i);
       }
       int anss=0;
       return anss;
   }
   else if(pos==1)
   {
       if(N==0) return 1;
       else return 0;
   }
}



