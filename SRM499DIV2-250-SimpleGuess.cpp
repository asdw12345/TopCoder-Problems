class SimpleGuess {
public:
	int getMaximum(vector <int>);
};

int SimpleGuess::getMaximum(vector <int> hints)
{
    int n=hints.size();
    int i,j;
    int maxx=0;
    for(i=0;i<n;i++)
    {
        for(j=i+1;j<n;j++)
        {
            int x=hints[i]+hints[j];
            int y=abs(hints[i]-hints[j]);
            if(x%2==1||y%2==1) continue;
            else
            {
                maxx=max(maxx,x*y/4);
            }
        }
    }
	return maxx;
}

