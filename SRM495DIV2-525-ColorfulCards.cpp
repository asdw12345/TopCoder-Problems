class ColorfulCards {
public:
	vector <int> theCards(int, string);
};
#define MAXN 1000+10
char prime[MAXN];
int num=0;
void primelist()
{
    int i,j;
    for(i=0;i<MAXN;i++) prime[i]='B';
    for(int i=2;i<=1000;i++)
    {
        int flag=1;
        for(j=2;j*j<=i;j++)
        {
            if(i%j==0)
            {
                flag=0;
                break;
            }
        }
        if(flag==1) prime[i]='R';
    }
}
int poss[MAXN],po[MAXN];
vector <int> ColorfulCards::theCards(int N, string colors)
{
	primelist();
	int n=colors.size();
	vector <int> ans(n,0);
	int i,pos=1;
	for(i=0;i<n;i++)
    {
        while(prime[pos]!=colors[i]) pos++;
        poss[i]=pos;
        pos++;
    }
    pos=N;
    for(i=n-1;i>=0;i--)
    {
        while(prime[pos]!=colors[i]) pos--;
        po[i]=pos;
        pos--;
    }
	for(i=0;i<n;i++)
    {
        if(po[i]!=poss[i]) ans[i]=-1;
        else ans[i]=poss[i];
    }
    return ans;

}

