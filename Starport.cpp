class Starport {
public:
	double getExpectedTime(int, int);
};

int GCD(int x,int y)
{
    if(x==0||y==0) return 1;
    return x%y==0?y:GCD(y,x%y);
}
double Starport::getExpectedTime(int N, int M)
{
    int gcd=GCD(max(N,M),min(N,M));
    return gcd*(N/gcd-1)*1.0/2.0;
}



